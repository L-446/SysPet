    @Override
    public void update(Observable o, Object obj) {
        Cliente c = (Cliente) obj;
        ArrayList<Cliente> newArrayList = new ArrayList<Cliente>();
        for (Cliente a : clientes) {
            if (a.getId() == c.getId()) {
                newArrayList.add(c);
            } else {
                newArrayList.add(a);
            }
        }
        clientes = newArrayList;
        fillTable();
        atualizaTabela();

    }
